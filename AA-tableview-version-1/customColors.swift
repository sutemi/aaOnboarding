import Foundation
import UIKit


extension UIColor {
    
    class func aaPrimaryBlue() -> UIColor {
        return UIColor(colorLiteralRed: 8/255, green: 93/255, blue: 169/255, alpha: 1.0)
    }
    
    class func aaTextBlue() -> UIColor {
        return UIColor(red: 0.0, green:0.0, blue:0.0, alpha:1.0)
    }
    
    class func aaWhite() -> UIColor {
        return UIColor(colorLiteralRed: 255/255, green: 255/255, blue: 255/255, alpha: 1.0)
    }
    
    class func aaLtGray() -> UIColor {
        return UIColor(colorLiteralRed: 235/255, green: 235/255, blue: 235/255, alpha: 1.0)
    }
    
    class func aaMdGray() -> UIColor {
        return UIColor(colorLiteralRed: 157/255, green: 166/255, blue: 171/255, alpha: 1.0)
    }
    

 }









